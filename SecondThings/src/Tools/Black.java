package Tools;

public class Black {
	private String username;
	private int id;
	
	public Black(int id,String username){
		this.username=username;
		this.id=id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
}
